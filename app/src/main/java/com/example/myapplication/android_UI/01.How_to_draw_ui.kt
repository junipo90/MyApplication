package com.example.myapplication.android_UI

// 안드로이드에서 화면을 그리는 방법
// UI는 XML을 이용한다
// XML -> DSL Language (Domain Specific Language)
// src - main - res - layout -> xml 파일이 만들어져있다

// 핸드폰 기종마다 화면 크기가 다른데 어떻게 그려야 할까
// 픽셀, dpi, dp 단위
// 픽셀 -> 화면에서 빛이 나오는 전구 하 -> 가장 작은 단위
// dpi -> dot per inch
//      - ldpi -> 120 (1인치에 120픽셀)
//      - mdpi -> 160
//      - hdpi -> 240
//      - xhdpi -> 320
//      - xxhdpi -> 480
//      - xxxhdpi -> 640
//      -> 고해상도 핸드폰일 수록 크기가 작게 보인
// dp -> Density Independent Pixel
//      - 픽셀 독립적인 단위 -> dp로 계산하면 어떤 핸드폰에서든 같은 크기로 보인다



